enum SyrianGovernorate {
  damascus('دمشق'),
  rifDimashq('ريف دمشق'),
  aleppo('حلب'),
  homs('حمص'),
  hama('حماة'),
  latakia('اللاذقية'),
  tartous('طرطوس'),
  idlib('إدلب'),
  raqqa('الرقة'),
  deirEzzor('دير الزور'),
  hasakah('الحسكة'),
  daraa('درعا'),
  sweida('السويداء'),
  quneitra('القنيطرة');

  final String value;
  const SyrianGovernorate(this.value);

  factory SyrianGovernorate.fromValue(String value) {
    return SyrianGovernorate.values.firstWhere(
      (e) => e.value == value,
      orElse: () => SyrianGovernorate.damascus,
    );
  }
}

